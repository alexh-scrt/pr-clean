class TestBuildNonOverlappingSpans:
    def _make_match(self, start: int, end: int, name: str = "p") -> ScanMatch:
        """Build a minimal ScanMatch for span testing."""
        from pr_clean.patterns import get_pattern_by_name, Severity

        pattern = get_pattern_by_name("copilot_agent_tips_block")
        assert pattern is not None
        return ScanMatch(
            pattern_name=name,
            pattern=pattern,
            matched_text="x" * (end - start),
            line_start=1,
            line_end=1,
            char_start=start,
            char_end=end,
            severity=Severity.HIGH,
            confidence=0.9,
        )

    def test_non_overlapping_spans_preserved(self) -> None:
        matches = [
            self._make_match(0, 10, "a"),
            self._make_match(20, 30, "b"),
        ]
        spans = _build_non_overlapping_spans(matches)
        assert len(spans) == 2
        assert spans[0][0] == 0
        assert spans[1][0] == 20

    def test_overlapping_spans_merged(self) -> None:
        matches = [
            self._make_match(0, 15, "a"),
            self._make_match(10, 25, "b"),
        ]
        spans = _build_non_overlapping_spans(matches)
        assert len(spans) == 1
        assert spans[0][0] == 0
        assert spans[0][1] == 25

    def test_identical_spans_deduplicated(self) -> None:
        matches = [
            self._make_match(5, 15, "a"),
            self._make_match(5, 15, "b"),
        ]
        spans = _build_non_overlapping_spans(matches)
        assert len(spans) == 1

    def test_empty_matches_returns_empty(self) -> None:
        assert _build_non_overlapping_spans([]) == []

    def test_adjacent_spans_not_merged(self) -> None:
        matches = [
            self._make_match(0, 10, "a"),
            self._make_match(10, 20, "b"),
        ]
        spans = _build_non_overlapping_spans(matches)
        assert len(spans) == 2


