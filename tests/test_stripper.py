class TestExpandSpanToFullLines:
    def test_single_line_match_expanded(self) -> None:
        text = "line one\nGENERATED BY COPILOT\nline three\n"
        # 'GENERATED BY COPILOT' starts at offset 9 (after 'line one\n')
        start = text.index("GENERATED")
        end = start + len("GENERATED BY COPILOT")
        exp_start, exp_end = _expand_span_to_full_lines(text, start, end, strip_full_block=True)
        # The expanded region should start at the beginning of the line.
        assert text[exp_start] == "G"  # first char of the matched line
        # Expanded end should consume the trailing newline.
        assert exp_end > end

    def test_no_expansion_when_strip_full_block_false(self) -> None:
        text = "line one\nGENERATED\nline three\n"
        start = text.index("GENERATED")
        end = start + len("GENERATED")
        exp_start, exp_end = _expand_span_to_full_lines(text, start, end, strip_full_block=False)
        assert exp_start == start
        assert exp_end == end

    def test_expansion_at_start_of_text(self) -> None:
        text = "MATCH\nmore text\n"
        start, end = 0, len("MATCH")
        exp_start, exp_end = _expand_span_to_full_lines(text, start, end, strip_full_block=True)
        assert exp_start == 0

    def test_expansion_at_end_of_text(self) -> None:
        text = "some text\nMATCH"
        start = text.index("MATCH")
        end = len(text)
        exp_start, exp_end = _expand_span_to_full_lines(text, start, end, strip_full_block=True)
        assert exp_end <= len(text) + 1  # May consume up to the end.


